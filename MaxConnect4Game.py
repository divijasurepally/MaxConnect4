    def minimax_function(self, board_state, depth, alpha_val, beta_val, winner=None):
        if winner is None:
            winner = beta_val
            beta_val = alpha_val
            depth = alpha_val
            board_state = depth
            board_state = self

        board_game = np.array(board_state)

        next_moves = []
        for col in range(total_column_nums):
            if board_game[0][col] == 0:
                next_moves.append(col)

        moves_finished = len(next_moves) == 0
        # print("moves_finished", moves_finished, depth)

        if depth == 0 or moves_finished:
            if moves_finished:
                if maxConnect4Game.get_winner(board_game, COM):
                    return (None, -1000000)
                elif maxConnect4Game.get_winner(board_game, HUMAN):
                    return (None, 1000000)
                else:# Game Draw
                    return (None, 0)
            else:
                return (None, maxConnect4Game.get_board_score(board_game, COM))

        if winner:
            weightage = -1000000
            col = random.choice(next_moves)
            for col in next_moves:
                for temp_row in range(total_row_nums):
                    if board_game[temp_row][col] == 0:
                        row =  temp_row
                temp_board = board_game.copy()
                temp_board[row][col] = COM
                latest_score = maxConnect4Game.minimax_function(temp_board, depth-1, alpha_val, beta_val, False)[1]
                if latest_score > weightage:
                    weightage = latest_score
                    col = col
                alpha_val = max(alpha_val, weightage)
                if alpha_val >= beta_val:
                    break
            return col, weightage

        else:
            value = 1000000
            column = random.choice(next_moves)
            for col in next_moves:
                for temp_row in range(total_row_nums):
                    if board_game[temp_row][col] == 0:
                        row =  temp_row
                temp_board_copy = board_game.copy()
                temp_board_copy[row][col] = COM
                new_score = maxConnect4Game.minimax_function(temp_board_copy, depth-1, alpha_val, beta_val, True)[1]
                if new_score < value:
                    value = new_score
                    column = col
                beta_val = min(beta_val, value)
                if alpha_val >= beta_val:
                    break
            return column, value


