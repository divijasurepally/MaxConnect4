    def get_winner(gameboard, piece):
        for vertical in range(total_column_nums-3):
            for horizontal in range(total_row_nums):
                if gameboard[horizontal][vertical] == piece and gameboard[horizontal][vertical+1] == piece and gameboard[horizontal][vertical+2] == piece and gameboard[horizontal][vertical+3] == piece:
                    return True

        for vertical in range(total_column_nums):
            for horizontal in range(total_row_nums-3):
                if gameboard[horizontal][vertical] == piece and gameboard[horizontal+1][vertical] == piece and gameboard[horizontal+2][vertical] == piece and gameboard[horizontal+3][vertical] == piece:
                    return True

        for vertical in range(total_column_nums-3):
            for horizontal in range(total_row_nums-3):
                if gameboard[horizontal][vertical] == piece and gameboard[horizontal+1][vertical+1] == piece and gameboard[horizontal+2][vertical+2] == piece and gameboard[horizontal+3][vertical+3] == piece:
                    return True

        for vertical in range(total_column_nums-3):
            for horizontal in range(3, total_row_nums):
                if gameboard[horizontal][vertical] == piece and gameboard[horizontal-1][vertical+1] == piece and gameboard[horizontal-2][vertical+2] == piece and gameboard[horizontal-3][vertical+3] == piece:
                    return True


   
    def get_board_score(board_state, current_player): #get the score of current board status
        board_state = board_state.tolist()
        gb = maxConnect4Game()
        gb.board_state = board_state
        
